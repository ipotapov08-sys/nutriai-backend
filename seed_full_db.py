import sqlite3
import os

DB_PATH = 'nutriai.db'

# Удаляем старую базу, если она есть
if os.path.exists(DB_PATH):
    os.remove(DB_PATH)

conn = sqlite3.connect(DB_PATH)
c = conn.cursor()

# Создаём таблицы заново
c.executescript('''
    CREATE TABLE IF NOT EXISTS users (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        email TEXT UNIQUE NOT NULL,
        password_hash TEXT NOT NULL,
        full_name TEXT NOT NULL,
        created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
    );
    
    CREATE TABLE IF NOT EXISTS metrics (
        user_id INTEGER PRIMARY KEY,
        gender TEXT,
        age INTEGER,
        weight_kg REAL,
        height_cm REAL,
        activity_level TEXT DEFAULT 'moderate',
        goal TEXT DEFAULT 'maintenance',
        updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
        FOREIGN KEY (user_id) REFERENCES users(id)
    );
    
    CREATE TABLE IF NOT EXISTS foods (
        id INTEGER PRIMARY KEY,
        name_ru TEXT NOT NULL,
        category TEXT,
        subcategory TEXT,
        calories REAL,
        protein REAL,
        fat REAL,
        carbs REAL,
        fiber REAL,
        water REAL
    );
    
    CREATE TABLE IF NOT EXISTS meals (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        name_ru TEXT NOT NULL,
        meal_type TEXT NOT NULL,
        diet_types TEXT DEFAULT 'omnivore,vegetarian,vegan,keto,mediterranean,low_carb',
        description TEXT,
        calories REAL NOT NULL,
        protein REAL NOT NULL,
        fat REAL NOT NULL,
        carbs REAL NOT NULL,
        ingredients TEXT,
        recipe TEXT
    );
    
    CREATE TABLE IF NOT EXISTS food_diary (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        user_id INTEGER,
        food_id INTEGER,
        grams REAL,
        meal_type TEXT,
        consumed_at DATE DEFAULT CURRENT_DATE,
        created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
        FOREIGN KEY (user_id) REFERENCES users(id)
    );
    
    CREATE TABLE IF NOT EXISTS nutri_pet (
        user_id INTEGER PRIMARY KEY,
        name TEXT DEFAULT 'Нутри',
        state TEXT DEFAULT 'neutral',
        level INTEGER DEFAULT 1,
        xp INTEGER DEFAULT 0,
        streak INTEGER DEFAULT 0,
        best_streak INTEGER DEFAULT 0,
        last_active DATE,
        created_at DATE DEFAULT CURRENT_DATE,
        FOREIGN KEY (user_id) REFERENCES users(id)
    );
    
    CREATE TABLE IF NOT EXISTS achievements (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        user_id INTEGER,
        achievement_id TEXT,
        unlocked_at DATE DEFAULT CURRENT_DATE,
        FOREIGN KEY (user_id) REFERENCES users(id)
    );
    
    CREATE TABLE IF NOT EXISTS daily_quests (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        user_id INTEGER,
        quest_id INTEGER,
        completed INTEGER DEFAULT 0,
        date DATE DEFAULT CURRENT_DATE,
        FOREIGN KEY (user_id) REFERENCES users(id)
    );
    
    CREATE TABLE IF NOT EXISTS water_tracking (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        user_id INTEGER,
        glasses INTEGER DEFAULT 0,
        date DATE DEFAULT CURRENT_DATE,
        FOREIGN KEY (user_id) REFERENCES users(id)
    );
    
    CREATE TABLE IF NOT EXISTS diet_tables (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        name TEXT NOT NULL,
        description TEXT,
        recommendations TEXT,
        restrictions TEXT,
        goal_relation TEXT
    );
''')

